<?php

namespace app\controllers;

use app\models\Role;
use app\models\User;
use app\models\Vacancy;
use Yii;
use yii\filters\AccessControl;
use yii\web\Controller;
use yii\web\Response;
use yii\filters\VerbFilter;
use app\models\LoginForm;
use app\models\ContactForm;

class SiteController extends AppController
{
    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'only' => ['logout'],
                'rules' => [
                    [
                        'actions' => ['logout'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }

    /**
     * Displays homepage.
     *
     * @return string
     */
    public function actionIndex()
    {
        $adminRole = Role::find()->where(['roleName' => 'admin'])->one();
        if(empty($adminRole)){
            $role = new Role();
            $role->roleName = 'admin';
            $role->save();
        }
        $admin = User::find()->where(['username' => 'admin'])->one();
        if(empty($admin)) {
            $newAdmin = new User();
            $newAdmin->username = 'admin';
            $newAdmin->lastName = 'Shtabvenko';
            $newAdmin->firstName = 'Ivan';
            $newAdmin->phone = '+99891-165-4667';
            $newAdmin->email = 'email@gmail.com';
            $newAdmin->role_id = $adminRole['id'];
            $newAdmin->setPassword('admin');
            $newAdmin->save();
        }

        $vacancies = Vacancy::find()->where(['status'=>'1'])->orderBy(['id'=>SORT_DESC])->limit(3)->all();
        return $this->render('index',compact('vacancies'));
    }

    /**
     * Login action.
     *
     * @return Response|string
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        }

        $model->password = '';
        return $this->render('login', [
            'model' => $model,
        ]);
    }

    /**
     * Logout action.
     *
     * @return Response
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->redirect('/admin/index');
    }

    /**
     * Displays contact page.
     *
     * @return Response|string
     */
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->contact(Yii::$app->params['adminEmail'])) {
            Yii::$app->session->setFlash('contactFormSubmitted');

            return $this->refresh();
        }
        return $this->render('contact', [
            'model' => $model,
        ]);
    }

    /**
     * Displays about page.
     *
     * @return string
     */
    public function actionAbout()
    {
        return $this->render('about');
    }

    public function actionHello()
    {
        return "about2";
    }
}
